package SeleniumClass;

public class TimeComplexity {

	public static void main(String[] args) {
		// More Optimized code, Optimized can be calculated by Time Complexity
		// Denoted by Big O:O(n)
		
		int i=1;
		System.out.println(i);
		//Time complexity of this code is One, which will be written as O(1)
		
		String s="hello";
		System.out.println(s);
		
		//Time Complexity- O(1)
		
		int a=1;
		int b=2;
		int c= a+b;
		System.out.println(c);
		//O(1)
		
		
	}
	
	

}
